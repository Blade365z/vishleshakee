        Clicking, on the button will provide the user the choice buttons for the analytical operations:
 
        <p class="helpHeadings m-0"> Centrality <p>
            In social networks, centrality is an indicator of identifying important nodes in a network. Our
            module provides the following variants of centrality:
            <br>
 
            <div>
              <figure class="figure">
                <img class="center" src="public/Screens/net3.PNG" id="netpics"/>
                <figcaption class="figure-caption text-center "> Network Centrality </figcaption>
              </figure>
            </div>
 
            <ol>
              <li>Degree Centrality</li>
              <li>Eigen Value Centrality</li>
              <li>Betweeness Centrality</li>
              <li>Page Rank Centrality</li>
            </ol>
 
            <br>
            For performing the centrality operation on the network. The following steps need to be
            followed:
 
            <ol>
              <li>Click on the blue colored Centrality Tab</li>
              <li>Select the algorithm choice for the centrality finding provided via the radio buttons.</li>
              <li>Click on the EXEC button.</li>
            </ol>
 
            <br>
            Upon success of the query the resultant graph will be displayed in the same panel. Zoom
            out the division for letting the graph to be displayed as a whole.
 
            <p class="helpHeadings m-0"> Community Detection <p>
                In social networks Community Detection aims to group similar nodes together, thus discovering hidden communities present. Our module provides the following variants of Community
                Detection
                <br>
 
                <div>
                  <figure class="figure">
                    <img class="center" src="public/Screens/net4.PNG" id="netpics"/>
                    <figcaption class="figure-caption text-center "> Community Detection </figcaption>
                  </figure>
                </div>
 
                <ol>
                  <li>Async Fluidic</li>
                  <li>Label Propagation</li>
                  <li> Grivan Newman </li>
                </ol>
                <br>
                Steps to be followed for finding out the communities in the graph
                <ol>
                  <li>Click on the community Detection Tab</li>
                  <li>Select the appropriate algorithm choice, if you select Async Fluidic as the algorithm you
                    need to provide the number of communities to be generated</li>
                 <li>Click on the EXEC button.</li>
                </ol>
                <br>
                In the above Community Detection sample output each color defines a community where the
                nodes with similar color belongs to the same community.
                Upon successful query a graph will be displayed in the same panel with communities in
                different colors.
 
                <p class="helpHeadings m-0"> Shortest Paths <p>
                    Shortest path aims to find a shortest path between a pair of nodes in the network. Our module
                    provides the following variants of Shortest Path.
 
                    <ol>
                      <li>Shortest Path</li>
                      <li>K-Shortest Path</li>
                      <li>All Possible Shortest Path per depth</li>
                    </ol>
                    <br>
                    Steps for finding the shortest path in the graph is as follows:
 
                    <ol>
                      <li> Click on the Shortest Path Tab</li>
                      <li> Provide Source and Destination node. One can provide this information directly in the
                        query box or by clicking on the nodes in the graph. The selection of two nodes from the graph:
                        First node can be selected by a direct single click while the selection of the next node
                        requires Cmd+click on the node.</li>
                      <li>Select the Algorithm choice and Click on the EXEC button</li>
                    </ol>
                    <br>
                    On successful query the source and target node will be enlarged also the edge between them
                    will be bold. If any intermediate node exists between the two nodes, then the intermediate
                    node will also be enlarged colored in different color from the source and destination node.
 
                    <p class="helpHeadings m-0"> Link Prediction <p>
                        Link prediction aims to predict future connections between nodes based upon their present
                        neighbbourhood. Our module provides the following variants of link prediction.
 
                        <div>
                          <figure class="figure">
                            <img class="center" src="public/Screens/net5.PNG" id="netpics"/>
                            <figcaption class="figure-caption text-center "> Link Prediction </figcaption>
                          </figure>
                        </div>
 
                        <ol>
                          <li>Adamic Adar</li>
                          <li>Jaccard Coefficient</li>
                        </ol>
 
                        The steps to be followed for the link prediction is as follows:
 
                        <ol>
                          <li>Click on the link prediction Tab</li>
                          <li>Select the Algorithm choice.</li>
                          <li>Select a node from the graph.</li>
                          <li>Provide the number of top k links to be predicted.</li>
                          <li>Click on the EXEC button</li>
                        </ol>
                        Upon successful query the query node and the predicted nodes will be highlighted in
                        different color and the predicted edges will be appearing in dark black color.
 
