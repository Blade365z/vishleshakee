    <div class="helpText">
      <p class="text-muted"> The Network Analysis module of Social Media Analysis Tool VISHLESHAKEE provides
        the functionality of analyzing different types of networks obtained from the system as well as
        networks that are being imported from the external source. As, the social media data is highly
        relational analysing them from the relational perspective can help gaining a better insight.
        The networks currently being supported by the system can be seen from the Network choice
        selector in the query panel. The module can generate a network given by the user with the required mandatory information and can perform various analysis on them.
        The rest of the manual provide the steps to be followed for generating a network and doing
        suitable analysis on them.
      </p>
    </div>
    
