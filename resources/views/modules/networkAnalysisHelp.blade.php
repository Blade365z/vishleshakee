          <figure class="figure">
            <img class="center" src="public/Screens/net2.PNG" id="netpics"/>
            <figcaption class="figure-caption text-center "> Network View Panel </figcaption>
          </figure>
        </div>
 
        Once, the loader disappears,
        The user is required to click on the view-graph text present on the card. Clicking on the
        text will show up the generated graph to the user in the Graph View Panel.
        Let the graph appear completely in the panel with all the edges and nodes rendered and
        wait till the graph becomes stable. It will take few seconds for the graph to be stable depending
        upon your system specifications.
        Note: It is to be noted that performing some analytical operations on the unstable graph may
        result in failing operations.)
