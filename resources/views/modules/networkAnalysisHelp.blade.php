      <p class=" helpHeadings m-0"> Network Generation <p>
 
          <p class="helpsubHeadings m-0"> Network Generation from User Inputs<p>
              For generating the network, a user is required to provide the following mandatory information.
 
              <div>
                <figure class="figure">
                    <img class="center" src="public/Screens/net1.PNG" id="netpics"/>
                    <figcaption class="figure-caption text-center "> Network Generation Panel </figcaption>
                </figure>
              </div>
 
 
              <ol>
                <li> Query: The query is the input given by the user it may be a hashtag or a mention or a
                  user ID, or a keyword. Sample Inputs: #JNU, @ndtv, narendra modi, cricket</li>
                <li>From Date: Starting date of the range query</li>
                <li>To Date: Ending date of the range query</li>
                <li> Network Size: The upper bound on the number of nodes to be generated for the
                  query. Say, 50 or 100.</li>
                <li>Network Choice: The selector provides the facility to generate different networks. One
                  need to select appropriate network to be generated for the given query</li>
              </ol>
 
              It is to be noted that the choice of the network should be compatible with the given query.
              i.e. if the given input query is a hashtag then the following networks should be only chosen
              for the network generation.
 
              <ol>
                <li>Hashtag-Hashtags.</li>
                <li>Hashtag-Mentions.</li>
                <li>Hashtag-User</li>
              </ol>
 
              Similarly, if the given input is a mention then the following networks are valid:
 
              <ol>
                <li>Mention-Mentions</li>
                <li>Mention-Hashtags.</li>
                <li>Mention-Users.</li>
              </ol>
    </div>
    
        Once, all the inputs are being provided the user is required to click on the Generate Network button
        placed by the side of the input box in the query panel. This will result in the graph to be
        generated. A card will show up down the query panel with the basic information about the
        network being generated. The loader inside the card appears marking the process running for
        generating the graph.
        Wait, till the loader disappears from the card.
    <p class="helpsubHeadings m-0"> Network Generation from External File Upload<p>
        A network can also be generated by importing data from external source. The module currently
        accepts .csv files as edgelist (in the format of [src,dst,count]) as an external source for network
        generation.
 
 
